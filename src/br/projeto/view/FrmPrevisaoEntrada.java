    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
//        String projeto = "%" + TxtNumOfPesq.getText() + "%";
        String status = "AGUARDANDO RECEBIMENTO";
        String status2 = "ENTREGA PARCIAL";
        String projeto = CbProjeto.getSelectedItem().toString();
        
        if (CbProjeto.getSelectedItem().equals("AREAS PROGRAMATICAS (AP'S)")){
            
           projeto = "AREA PROGRAMATICA%";                                   
        }
                        
        //JOptionPane.showMessageDialog(null, projeto);
        DadosPedidosDAO dao = new DadosPedidosDAO();
        List<DadosPedidos> lista = dao.buscaPedidosParaPrevisao(projeto,status,status2);
        DefaultTableModel dados = (DefaultTableModel) tblPrevisao.getModel();
        dados.setNumRows(0);

        for (DadosPedidos d : lista) {
            dados.addRow(new Object[]{
                d.getId(),
                d.getCod(),
                d.getItem(),
                d.getPrevisão(),
                d.getStatus(),
                
                
            });
        }
    }//GEN-LAST:event_jButton2ActionPerformed

