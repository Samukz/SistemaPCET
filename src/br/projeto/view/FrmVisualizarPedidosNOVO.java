    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

    String nome = TxtPesquisa.getText();
    String status = "AGUARDANDO DADOS DO PEDIDO";
    
    InPedidosDAO dao = new InPedidosDAO();
    List<InPedidos> lista = dao.buscaPedidoPorNumero2(nome, status);
    DefaultTableModel dados = (DefaultTableModel) TblVisualizarPedidos.getModel();
    dados.setNumRows(0);

    for (InPedidos d : lista) {
        dados.addRow(new Object[]{
            d.getId(),
            d.getData_pedido(),
            d.getCentro_custo(),
            d.getFuncionario(),
            d.getNum_pedido_sica(), 
            d.getNumeroOF(),
            d.getCod(),
            d.getItem(),
            d.getQtd(),
            d.getPrevisao(),
            d.getFornecedor(),
            
            d.getStatus_pedido(),
        });
    }//GEN-LAST:event_jButton1ActionPerformed
    }
