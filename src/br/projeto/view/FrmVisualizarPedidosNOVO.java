    private void TblVisualizarPedidosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TblVisualizarPedidosMouseClicked
        // TODO add your handling code here:
        
       String textoDoLabel1 = lblusuario.getText();

DefaultTableModel modelo = (DefaultTableModel) TblVisualizarPedidos.getModel();

int linhaSelecionada = TblVisualizarPedidos.getSelectedRow();
int colunaSelecionada = TblVisualizarPedidos.getColumnModel().getColumnIndex("Nº PEDIDO");
int colunaSelecionada2 = TblVisualizarPedidos.getColumnModel().getColumnIndex("ID");

Object valorSelecionado = modelo.getValueAt(linhaSelecionada, colunaSelecionada);
String textoSelecionado = valorSelecionado.toString();

Object valorSelecionado2 = modelo.getValueAt(linhaSelecionada, colunaSelecionada2);
String textoSelecionado2 = valorSelecionado2.toString();

FrmVisualizarPedidosInteiro novoFormulario = new FrmVisualizarPedidosInteiro();

novoFormulario.TxtPedidoSica.setText(textoSelecionado);
novoFormulario.TxtId.setText(textoSelecionado2);

novoFormulario.setUsuario(textoDoLabel1);

novoFormulario.setVisible(true);

        
    }//GEN-LAST:event_TblVisualizarPedidosMouseClicked

